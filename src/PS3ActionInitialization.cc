/// \file PS3ActionInitialization.cc
/// \brief Implementation of the PS3ActionInitialization class

#include "PS3ActionInitialization.hh"
#include "PS3PrimaryGeneratorAction.hh"
#include "PS3RunAction.hh"
#include "PS3EventAction.hh"
#include "PS3SteppingAction.hh"

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

PS3ActionInitialization::PS3ActionInitialization()
 : G4VUserActionInitialization()
{}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

PS3ActionInitialization::~PS3ActionInitialization()
{}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

void PS3ActionInitialization::BuildForMaster() const
{
  PS3RunAction* runAction = new PS3RunAction;
  SetUserAction(runAction);
}

//....oooOO0OOooo........oooOO0OOooo........oooOO0OOooo........oooOO0OOooo......

void PS3ActionInitialization::Build() const
{
  SetUserAction(new PS3PrimaryGeneratorAction);

  PS3RunAction* runAction = new PS3RunAction;
  SetUserAction(runAction);
  
  PS3EventAction* eventAction = new PS3EventAction(runAction);
  SetUserAction(eventAction);
  
  SetUserAction(new PS3SteppingAction(eventAction));
}  

